#include "libft.h"

int	ft_atoi(const char *str)
{
	int				i;
	int				s;
	unsigned long	n;

	i = 0;
	s = 1;
	n = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = s * (-1);
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + str[i] - 48;
		if (n >= 9223372036854775807 && s > 0)
			return (-1);
		if (n > 9223372036854775807 && s < 0)
			return (0);
		i++;
	}
	return (n * s);
}
